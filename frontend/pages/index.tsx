import { useState, useEffect } from 'react';
import type { NextPage } from 'next';
import Head from 'next/head';
import { Grow, CircularProgress } from '@mui/material';

import { Post } from '../components';
import { getPosts } from '../state/actions/posts';
import { useDispatch, useSelector } from 'react-redux';
import { EditForm } from '../containers';
import { PostTypeState } from '../resources/interfaces';
import { GridItem, MainContainer, MainGrid } from '../styles/indexStyled'

const Home: NextPage = () => {
  const [open, setOpen] = useState(false);
  const [currentId, setCurrentId] = useState(null);
  const posts = useSelector(
    (state: PostTypeState) => state
  );
  const dispatch = useDispatch();

  useEffect(() => {
    dispatch(getPosts());
  }, [currentId, dispatch]);

  const handleClickOpen = () => {
    setOpen(true);
  };

  const handleClose = () => {
    setCurrentId(null);
    setOpen(false);
  };

  return (
    <>
      <Head>
        <title>IT Memes World</title>
        <meta name='description' content='Generated by create next app' />
        <link rel='icon' href='/favicon.ico' />
      </Head>

      <MainContainer>
        <Grow in>
          <MainGrid
            container={true}
            direction='column-reverse'
            justifyContent='space-between'
            alignItems='stretch'
            spacing={3}
          >
              {!posts?.posts.length ? (
                <CircularProgress />
              ) : (
                    posts.posts.map((post: any) => (
                    <GridItem item key={post.id} xs={12} sm={12}>
                      <Post
                        post={post}
                        currentId={currentId}
                        setCurrentId={setCurrentId}
                        handleClickOpen={handleClickOpen}
                      />
                    </GridItem>
                  ))
              )}
              {open && (
                <EditForm
                  currentId={currentId}
                  handleClose={handleClose}
                  open={open}
                />
              )}
            </MainGrid>
        </Grow>
      </MainContainer>
    </>
  );
};

export default Home;
